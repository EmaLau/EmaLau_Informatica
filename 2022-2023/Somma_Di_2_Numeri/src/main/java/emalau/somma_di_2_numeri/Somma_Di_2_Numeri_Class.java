public class Somma_Di_2_Numeri_Class {

    private double n1;
    private double n2;

    public void get_n1(double n1) {
        this.n1 = n1;
    }

    public void get_n2(double n2) {
        this.n2 = n2;
    }

    public double set_n1() {
        return this.n1;
    }

    public double set_n2() {
        return this.n2;
    }

    public void get_Addizione() {
    }

    public double set_Addizione() {
        return this.n1 + this.n2;
    }

    public void get_Sottrazione() {
    }

    public double set_Sottrazione() {
        return this.n1 - this.n2;
    }

    public void get_Divisione() {
    }

    public double set_Divisione() {
        return this.n1 / this.n2;
    }

    public void get_Moltiplicazione() {
    }

    public double set_Moltiplicazione() {
        return this.n1 * this.n2;
    }

}
